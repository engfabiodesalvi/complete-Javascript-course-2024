// 5. For..of
// Itera sobre valores de objetos iteráveis (como Array, Map, Set, String, etc.).

const cores = ['Vermelho', 'Azul', 'Verde'];

for (let cor of cores) {
    console.log(cor);
}

const receitas = ['nhoc', 'spaguete', 'bife a milanesa', 'bolo de fubá', 'arroz doce'];

for (let receita of receitas) {
    console.log(receita);
}